# Get unique subject_ids split by mortality status
dead_ids <- ds_binned %>%
  filter(is_dead == 1) %>%
  distinct(subject_id) %>%
  pull(subject_id)

alive_ids <- ds_binned %>%
  filter(is_dead == 0) %>%
  distinct(subject_id) %>%
  pull(subject_id)

# Randomly sample 100 subject_ids from each group
sampled_dead_ids <- sample(dead_ids, 600)
sampled_alive_ids <- sample(alive_ids, 1000)

# Filter the dataset to just these 200 subjects, keeping only relevant columns
plot_data <- ds_binned %>%
  filter(subject_id %in% c(sampled_dead_ids, sampled_alive_ids)) %>%
  select(subject_id, binned_hrs_since_icu, map, is_dead) %>%
  mutate(mortality_status = factor(if_else(is_dead == 1, "Died", "Survived"),
                                   levels = c("Died", "Survived")))

# Plot MAP trajectories, faceted by mortality status, with linear mean trend line overlaid
p_map_trajectories <- ggplot(plot_data, aes(x = binned_hrs_since_icu, y = map, group = subject_id, color = mortality_status)) +
  geom_line(alpha = 0.3, linewidth = 0.4) +
  geom_smooth(
    aes(group = mortality_status),
    method = "lm",
    se = TRUE,
    linewidth = 1.2,
    color = "black"
  ) +
  facet_wrap(~ mortality_status) +
  scale_color_manual(values = c("Died" = "orange", "Survived" = "lightblue")) +
  labs(
    title = "MAP Trajectories: 100 Random Patients Who Died vs. Survived (Binned)",
    x = "Hours Since ICU Admission (Binned)",
    y = "Mean Arterial Pressure (mmHg)"
  ) +
  theme_minimal() +
  theme(legend.position = "none")

